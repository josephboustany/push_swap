#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_node;
	t_list	*mapped_list;

	mapped_list = NULL;
	while (lst)
	{
		new_node = ft_lstnew(f(lst->content));
		if (!new_node)
		{
			ft_lstclear(&mapped_list, del);
			return (0);
		}
		ft_lstadd_back(&mapped_list, new_node);
		lst = lst->next;
	}
	return (mapped_list);
}
